def _normalize(name: str) -> str:
    """Lowercase, strip, collapse spaces, remove punctuation variants."""
    n = name.lower().strip()
    n = re.sub(r"[''']", "'", n)          # normalise apostrophes
    n = re.sub(r"\s+", " ", n)            # collapse whitespace
    n = re.sub(r"^(classic|iba|official|canonical)\s+", "", n)
    return n


def lookup_iba_recipe(drink_name: str) -> dict | None:
    """
    Look up a cocktail by name in IBA_RECIPES.
    Returns the recipe dict on a match, None otherwise.

    Strategy:
      1. Alias table (catches plurals, abbreviations, Chinese names)
      2. Direct key match (slug form)
      3. Exact English title match
      4. Exact Chinese title match
      5. Substring match in English title (prefer shorter titles)
    """
    if not drink_name or not drink_name.strip():
        return None

    norm = _normalize(drink_name)

    # 1. Alias table
    key = RECIPE_ALIASES.get(norm)
    if key and key in IBA_RECIPES:
        return IBA_RECIPES[key]

    # Also try stripping trailing 's' for plurals
    if norm.endswith("s") and len(norm) > 3:
        key = RECIPE_ALIASES.get(norm[:-1])
        if key and key in IBA_RECIPES:
            return IBA_RECIPES[key]

    # 2. Direct key match (slug form)
    slug = re.sub(r"[^a-z0-9]+", "_", norm).strip("_")
    if slug in IBA_RECIPES:
        return IBA_RECIPES[slug]

    # 3. Exact English title match (case-insensitive)
    for recipe in IBA_RECIPES.values():
        if recipe["title_en"].lower() == norm:
            return recipe

    # 4. Exact Chinese title match
    for recipe in IBA_RECIPES.values():
        if recipe.get("title_zh", "") == drink_name.strip():
            return recipe

    # 5. Substring match in English title (pick shortest matching title)
    candidates = [
        r for r in IBA_RECIPES.values()
        if norm in r["title_en"].lower()
    ]
    if candidates:
        return min(candidates, key=lambda r: len(r["title_en"]))

    return None


def iba_spec_to_render_spec(recipe: dict, language: str) -> dict:
    """
    Convert an IBA_RECIPES entry into the spec dict that render_recipe() expects.
    Sets spec['_confidence'] = 'iba_certified' to trigger the confidence badge.
    Builds bilingual ingredient names for ZH mode.
    """
    zh = language == "zh"

    # Build ingredient list with bilingual names + inline props
    ings: list[dict] = []
    for ing_src in recipe["ingredients"]:
        name_en = ing_src["name"]
        name_zh = _ING_ZH.get(name_en, "")
        if zh and name_zh:
            display_name = f"{name_en} / {name_zh}"
        else:
            display_name = name_en
        ings.append({
            "name": display_name,
            "amount_ml": float(ing_src["amount_ml"]),
            "category": ing_src["category"],
            "prep_note": ing_src.get("prep_note", ""),
            "props": ing_src.get("props") or _p(name_en),
        })

    # Language-specific fields
    if zh:
        title_primary = recipe["title_zh"]
        title_secondary = recipe["title_en"]
        tagline = recipe["tagline_zh"]
        garnish = recipe.get("garnish_zh", recipe.get("garnish_en", ""))
        method_steps = recipe["method_steps_zh"]
        science_note = recipe["science_note_zh"]
        architect_note = recipe["architect_note_zh"]
    else:
        title_primary = recipe["title_en"]
        title_secondary = recipe.get("title_zh", "")
        tagline = recipe["tagline_en"]
        garnish = recipe.get("garnish_en", "")
        method_steps = recipe["method_steps_en"]
        science_note = recipe["science_note_en"]
        architect_note = recipe["architect_note_en"]

    iba_cat = recipe.get("iba_category", "IBA Official")
    source_tag = f"[{iba_cat}]" if iba_cat != "Verified Classic" else "[Verified Classic]"

    return {
        "title_primary": title_primary,
        "title_secondary": title_secondary,
        "tagline": tagline,
        "technique": recipe["technique"],
        "glassware": recipe["glassware"],
        "garnish": garnish,
        "ingredients": ings,
        "method_steps": method_steps,
        "science_note": f"{source_tag} {science_note}",
        "architect_note": architect_note,
        "_confidence": "iba_certified" if iba_cat != "Verified Classic" else "verified_classic",
        "_iba_source": iba_cat,
    }
