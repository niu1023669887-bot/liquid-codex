def _p(name: str) -> dict:
    """Return inline props for ingredient `name`, falling back to empty dict."""
    return _PROPS.get(name, {})


def _ing(name: str, ml: float, cat: str, prep: str = "") -> dict:
    """Build a compact ingredient entry for IBA_RECIPES."""
    return {"name": name, "amount_ml": ml, "category": cat, "prep_note": prep, "props": _p(name)}


