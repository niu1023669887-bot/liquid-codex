// ── Core i18n engine ──

let _currentLang = 'en';

function t(key) {
  const val = TRANSLATIONS[_currentLang]?.[key] ?? TRANSLATIONS.en?.[key];
  return val ?? null;
}

function applyOptionTranslations(root) {
  root = root || document;
  root.querySelectorAll('option[data-i18n]').forEach(el => {
    const val = t(el.getAttribute('data-i18n'));
    if (val != null) el.textContent = val;
  });
}

function applyTranslations(root) {
  root = root || document;
  root.querySelectorAll('[data-i18n]').forEach(el => {
    if (el.tagName === 'OPTION') return;
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val != null) el.innerHTML = val;
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key);
    if (val != null) el.placeholder = val;
  });
  root.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = t(key);
    if (val != null) el.title = val;
  });
  applyOptionTranslations(root);
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  _currentLang = lang;
  localStorage.setItem('lc_lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  applyTranslations();
  updateLangToggle();
  if (typeof refreshMaterialCounts === 'function') refreshMaterialCounts();
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

function initLang() {
  const saved = localStorage.getItem('lc_lang') || 'en';
  _currentLang = saved;
  document.documentElement.lang = saved === 'zh' ? 'zh-CN' : 'en';
  applyTranslations();
  updateLangToggle();
}

function tr(key, params) {
  let s = t(key);
  if (s == null) return null;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      s = s.replaceAll('{' + k + '}', String(v));
    });
  }
  return s;
}

window.t = t;
window.tr = tr;
window.setLang = setLang;

function toggleLang() {
  setLang(_currentLang === 'en' ? 'zh' : 'en');
}

function updateLangToggle() {
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.textContent = _currentLang === 'en' ? '中文' : 'EN';
    btn.title = _currentLang === 'en' ? 'Switch to Chinese' : '切换为英文';
  });
}

// Auto-initialise after DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLang);
} else {
  initLang();
}
